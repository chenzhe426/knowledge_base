from datasets import load_dataset
from transformers import (
    AutoModelForCausalLM,
    AutoTokenizer,
    TrainingArguments,
    Trainer,
    DataCollatorForLanguageModeling,
)
from peft import LoraConfig, get_peft_model, TaskType
import torch


def preprocess(example):
    """Format messages into a single string."""
    messages = example.get("messages", [])
    if not messages:
        return {"text": ""}

    text_parts = []
    for msg in messages:
        role = msg.get("role", "")
        content = msg.get("content", "")
        if role == "user":
            text_parts.append(f"<|im_start|>user\n{content}<|im_end|>")
        elif role == "assistant":
            text_parts.append(f"<|im_start|>assistant\n{content}<|im_end|>")
        elif role == "system":
            text_parts.append(f"<|im_start|>system\n{content}<|im_end|>")

    text_parts.append("<|im_end|>")
    return {"text": "".join(text_parts)}



def main():
    model_id = "meta-llama/Llama-3.1-8B-Instruct"
    data_path = "evals/training/hf_output/data/train_huggingface.jsonl"

    print(f"Loading model: {model_id}")
    tokenizer = AutoTokenizer.from_pretrained(model_id, trust_remote_code=True)
    tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        model_id,
        device_map="auto",
        torch_dtype=torch.bfloat16 if True else torch.float16,
        trust_remote_code=True,
    )

    # Apply LoRA
    lora_config = LoraConfig(
        r=8,
        lora_alpha=16,
        target_modules="all-linear",
        task_type=TaskType.CAUSAL_LM,
        lora_dropout=0.05,
    )
    model = get_peft_model(model, lora_config)
    model.print_trainable_parameters()

    # Load data
    raw_dataset = load_dataset("json", data_files=data_path, split="train")
    processed = raw_dataset.map(
        preprocess,
        remove_columns=raw_dataset.column_names,
        desc="Tokenizing",
    )

    # Split
    if 0.0 > 0:
        split = processed.train_test_split(test_size=0.0)
        train_data = split["train"]
        eval_data = split["test"]
    else:
        train_data = processed
        eval_data = None

    def tokenize(example):
        result = tokenizer(
            example["text"],
            truncation=True,
            max_length=2048,
            padding="max_length",
        )
        result["labels"] = result["input_ids"].copy()
        return result

    train_data = train_data.map(tokenize, batched=True)
    if eval_data:
        eval_data = eval_data.map(tokenize, batched=True)

    training_args = TrainingArguments(
        output_dir="evals/training/hf_output",
        per_device_train_batch_size=1,
        gradient_accumulation_steps=8,
        max_steps=10,
        learning_rate=0.0001,
        num_train_epochs=1,
        warmup_ratio=0.1,
        weight_decay=0.0,
        logging_steps=10,
        save_steps=100,
        bf16=True,
        fp16=not True,
        report_to=["tensorboard"],
        save_total_limit=3,
    )

    data_collator = DataCollatorForLanguageModeling(tokenizer, mlm=False)

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_data,
        eval_dataset=eval_data,
        data_collator=data_collator,
    )

    trainer.train()

    # Save final model
    model.save_pretrained("evals/training/hf_output/final")
    print(f"Training complete. Model saved to {output_dir}/final")


if __name__ == "__main__":
    main()
